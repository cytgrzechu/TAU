import math

class Calculator:
    def add(self, x, y):
        return x + y


    def subtract(self, x, y):
        return x - y


    def divide(self, x, y):   
        return x / y


    def multiply(self, x, y):
        return x * y


    def square(self,x):
        return math.sqrt(x)


    def power(self, x, y):
        return math.pow(x,y) 